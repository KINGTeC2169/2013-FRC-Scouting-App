package com.rtzoeller.frcscouting;

public class Match {
	public int matchNumber;
	public int red1;
	public int red2;
	public int red3;
	public int blue1;
	public int blue2;
	public int blue3;
	
	public Match(int matchNumber, int red1, int red2, int red3, int blue1, int blue2, int blue3) {
		super();
		this.matchNumber = matchNumber;
		this.red1 = red1;
		this.red2 = red2;
		this.red3 = red3;
		this.blue1 = blue1;
		this.blue2 = blue2;
		this.blue3 = blue3;
	}
}
